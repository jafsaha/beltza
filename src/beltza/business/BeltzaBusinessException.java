package beltza.business;

public class BeltzaBusinessException extends RuntimeException {
	
	public BeltzaBusinessException() {
		super();
	}

	public BeltzaBusinessException(String message) {
		super(message);
	}

	public BeltzaBusinessException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public BeltzaBusinessException(Throwable cause) {
		super(cause);
	}
		
}
