package beltza.dao;

public class BeltzaDaoException extends Throwable {

	public BeltzaDaoException() {
		super();
	}

	public BeltzaDaoException(String message) {
		super(message);
	}

	public BeltzaDaoException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public BeltzaDaoException(Throwable cause) {
		super(cause);
	}
	
	
}
